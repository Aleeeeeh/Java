package funcoes_matematicas;

import javax.swing.JOptionPane;

public class funcoes {
	public static void main (String args[]) {
		double delta,a,b,c;
		double x1,x2;
	a=Double.parseDouble(JOptionPane.showInputDialog(null,"Digite valor de A "));
	b=Double.parseDouble(JOptionPane.showInputDialog(null,"Digite Valor de B: "));
	c=Double.parseDouble(JOptionPane.showInputDialog(null,"Digite Valor de c: "));

delta = Math.pow(b, 2.0) - 4*a*c;

x1 = (-b + Math.sqrt (delta)) / (2.0 * a);
x2 = (-b - Math.sqrt (delta)) /  (2.0 * a);

JOptionPane.showMessageDialog(null,"Resultado de x1" + x1);
JOptionPane.showMessageDialog(null,"Resultado de x2: " + x2);

	}
	}